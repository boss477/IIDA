export var SOFA_COLOR_OPTIONS = Object.keys(PALETTES)
  .filter(function (k) {
    return k !== "default";
  })
  .sort()
  .map(function (id) {
    return { id: id, label: sofaColorLabel(id) };
  });

export function parseSofaColor(keywords, productName) {
  var all = parseAllSofaColors(keywords, productName);
  return all.length ? all[0] : null;
}

/** Every color keyword mentioned in catalog text (first rule win per id). */
export function parseAllSofaColors(keywords, productName) {
  var text = [keywords, productName].filter(Boolean).join(" ");
  if (!text) return [];
  var found = [];
  var seen = {};
  for (var i = 0; i < COLOR_RULES.length; i++) {
    var id = COLOR_RULES[i].id;
    if (!seen[id] && COLOR_RULES[i].re.test(text)) {
      seen[id] = true;
      found.push(id);
    }
  }
  return found;
}

/**
 * Colors available for this SKU: detected on row + siblings with same product_name,
 * or the popular palette when none are listed in the DB.
 */
export function getAvailableSofaColors(catalogRow, catalog) {
  if (!catalogRow) return POPULAR_SOFA_COLORS.slice();
  var colors = parseAllSofaColors(catalogRow.keywords, catalogRow.product_name);
  var family = String(catalogRow.product_name || "")
    .split(/\s+/)[0]
    .toLowerCase();
  if (catalog && family) {
    catalog.forEach(function (row) {
      if (!row || !isSofaRow(row)) return;
      var pn = String(row.product_name || "").toLowerCase();
      if (pn === family || pn.indexOf(family) === 0) {
        parseAllSofaColors(row.keywords, row.product_name).forEach(function (c) {
          if (colors.indexOf(c) < 0) colors.push(c);
        });
      }
    });
  }
  if (!colors.length) return POPULAR_SOFA_COLORS.slice();
  return colors;
}

function isSofaRow(row) {
  var c = String(row.category || "").toLowerCase();
  return c.indexOf("sofa") >= 0 || c.indexOf("lounge") >= 0;
}

export function getSofaPalette(colorId) {
  if (!colorId) return PALETTES.default;
  if (colorId === "light_grey") {
    return {
      back: "#8a9199",
      arm: "#b4bac2",
      cushion: "#d8dde3",
      lounge: "#b4bac2",
      stroke: "#5c6370",
    };
  }
  if (colorId === "dark_grey") {
    return PALETTES.charcoal;
  }
  return PALETTES[colorId] || PALETTES.default;
}

export function sofaColorLabel(colorId) {
  if (!colorId) return "";
  return colorId.replace(/_/g, " ");
}
